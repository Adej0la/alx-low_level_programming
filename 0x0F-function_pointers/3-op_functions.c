#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: The answer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts one integer from another integer
 * @a: first integer
 * @b: second integer
 *
 * Return: The answer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: The answer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides one integer by another integer
 * @a: first integer
 * @b: second integer
 *
 * Return: The answer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds the modulus of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: The answer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
